package ejercicio4dos;

public class Mayuscula {

	public static void main(String[] args) {
		String letra1 = "A";
		String letra2 = "b";
		if (letra1.equals(letra1.toLowerCase())) {
			System.out.println(letra1 + " es minuscula");
		}else {
			System.out.println(letra1 + " es mayuscula");
		}
		if (letra2.equals(letra2.toLowerCase())) {
			System.out.println(letra2 + " es minuscula");
		}else {
			System.out.println(letra2 + " es mayuscula");
		}

	}

}
